<template>
  <div class="generator-wireframe">
    <h1>Generate Paragraphs with AI!</h1>
    <p class="subtitle">Input a topic and instantly get paragraphs generated by AI.</p>

    <!-- Topic Input -->
    <div class="input-group">
      <input 
        v-model="topic"
        type="text"
        placeholder="üîç Enter your topic..."
        class="topic-input"
        @keyup.enter="generateContent"
      />
    </div>

    <!-- Dropdowns Row -->
    <div class="dropdowns-row">
      <select v-model="contentType" class="dropdown">
        <option disabled value="">Content Type</option>
        <option value="paragraph">Paragraph</option>
        <option value="essay">Essay</option>
        <option value="letter">Letter</option>
        <option value="summary">Summary</option>
        <option value="report">Report</option>
      </select>

      <select v-model="tone" class="dropdown">
        <option disabled value="">Tone</option>
        <option value="academic">Academic</option>
        <option value="casual">Casual</option>
        <option value="professional">Professional</option>
        <option value="creative">Creative</option>
        <option value="persuasive">Persuasive</option>
      </select>

      <select v-model="length" class="dropdown">
        <option disabled value="">Length</option>
        <option value="short">Short</option>
        <option value="medium">Medium</option>
        <option value="long">Long</option>
      </select>
    </div>

    <!-- Generate Button -->
    <button 
      @click="generateContent"
      :disabled="loading || !topic.trim()"
      class="generate-btn"
    >
      {{ loading ? 'Generating...' : 'Generate' }}
    </button>

    <!-- Error Message -->
    <div v-if="error" class="error-message">
      ‚ö†Ô∏è {{ error }}
    </div>

